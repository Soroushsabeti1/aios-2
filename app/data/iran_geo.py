# نگاشت معکوس: شهر → استان
CITY_TO_PROVINCE = {}
for province, cities in IRAN_PROVINCES.items():
    for city in cities:
        CITY_TO_PROVINCE[city] = province

ALL_CITIES = list(CITY_TO_PROVINCE.keys())
ALL_PROVINCES = list(IRAN_PROVINCES.keys())


def find_province(city_name: str) -> str | None:
    """استان یک شهر را برمی‌گرداند."""
    return CITY_TO_PROVINCE.get(city_name.strip())


def is_valid_city(city_name: str) -> bool:
    return city_name.strip() in CITY_TO_PROVINCE


def cities_in_province(province_name: str) -> list[str]:
    return IRAN_PROVINCES.get(province_name.strip(), [])
