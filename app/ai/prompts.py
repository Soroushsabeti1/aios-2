_TONES = {
    "friendly": {
        "desc": "صمیمی و محاوره‌ای — مثل یه دوست باهوش",
        "emoji_rule": "گاهی از ایموجی استفاده کن — نه زیاد",
    },
    "formal": {
        "desc": "رسمی و حرفه‌ای — محترمانه و دقیق",
        "emoji_rule": "ایموجی فقط در مواقع ضروری",
    },
    "neutral": {
        "desc": "خنثی و کاری — بدون صمیمیت اضافه",
        "emoji_rule": "بدون ایموجی",
    },
}


def get_system_prompt(person_role: str = None, settings: dict = None) -> str:
    """سیستم پرامپت با لحن از TenantSettings."""
    s = settings or {}
    tone = s.get("tone", "friendly")
    ai_name = s.get("ai_name", "دستیار هوشمند")
    biz_name = s.get("biz_name", "مجموعه")
    person_name = s.get("person_name", "")

    tone_info = _TONES.get(tone, _TONES["friendly"])
    tone_desc = tone_info["desc"]
    emoji_rule = tone_info["emoji_rule"]

    base = _BASE_PROMPT.format(
        ai_name=ai_name,
        tone_desc=tone_desc,
        emoji_rule=emoji_rule,
    )
    base += f"\n\n📅 {_today_info()}"

    if person_role in ("employee", "collaborator"):
        tmpl = _TONE_EMPLOYEE if person_role == "employee" else _TONE_COLLABORATOR
        return base + "\n\n" + tmpl.format(
            name=person_name or "کاربر",
            biz_name=biz_name,
            tone_desc=tone_desc,
        )
    if person_role == "customer":
        return base + "\n\n" + _TONE_CUSTOMER.format(
            name=person_name or "مشتری",
            biz_name=biz_name,
            tone_desc=tone_desc,
        )
    if person_role == "partner":
        return base + "\n\n" + _TONE_PARTNER.format(
            name=person_name or "همکار",
            biz_name=biz_name,
        )
    return base


SYSTEM_PROMPT = get_system_prompt()
