from datetime import datetime, timezone, timedelta

IRAN_OFFSET = timedelta(hours=3, minutes=30)


def _today_info() -> str:
    now = datetime.now(timezone.utc) + IRAN_OFFSET
    weekdays = ["دوشنبه", "سه‌شنبه", "چهارشنبه", "پنجشنبه",
                "جمعه", "شنبه", "یکشنبه"]
    wd = weekdays[now.weekday()]
    return (f"امروز {wd} است. تاریخ میلادی امروز: {now.strftime('%Y-%m-%d')} "
            f"و ساعت فعلی به وقت ایران: {now.strftime('%H:%M')}.")


